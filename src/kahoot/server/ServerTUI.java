    private static void criarNovoJogo(Scanner scanner, int id, List<Question> perguntas) {
        System.out.println("\n--- üõ†Ô∏è CONFIGURAR JOGO " + id + " ---");

        int numEquipas = 0;
        try {
            System.out.print("üî¢ Quantas equipas v√£o jogar? ");
            numEquipas = Integer.parseInt(scanner.nextLine());
        } catch (NumberFormatException e) {
            System.out.println("‚ùå N√∫mero inv√°lido.");
            return;
        }

        Map<String, Team> equipasDesteJogo = new HashMap<>();

        for (int i = 1; i <= numEquipas; i++) {
            System.out.print("üìõ Nome da Equipa " + i + ": ");
            String nome = scanner.nextLine();
            if (nome.isBlank()) nome = "Equipa " + i;

            // Gera c√≥digo aleat√≥rio de 4 caracteres
            String codigo = UUID.randomUUID().toString().substring(0, 4).toUpperCase();

            Team t = new Team(nome);
            equipasDesteJogo.put(codigo, t);

            System.out.println("   ‚úÖ Criada: [" + nome + "] -> C√ìDIGO: " + codigo);
        }

        // Criar o identificador do jogo
        String idJogo = "JOGO-" + id;

        // 1. Criar a Sala (GameRoom)
        // Passamos uma c√≥pia das perguntas para garantir independ√™ncia
        GameRoom novaSala = new GameRoom(idJogo, equipasDesteJogo, new ArrayList<>(perguntas));

        // 2. Registar a Sala na Rece√ß√£o (GameServer)
        // Isto diz ao servidor central: "Quem vier com o c√≥digo X, manda para a sala JOGO-Y"
        GameServer.registarNovoJogo(novaSala, equipasDesteJogo);

        System.out.println("\n‚ú® JOGO CRIADO COM SUCESSO!");
        System.out.println("üìã Partilha estes c√≥digos com os jogadores:");
        for (var entry : equipasDesteJogo.entrySet()) {
            System.out.println("   üîë C√≥digo: " + entry.getKey() + "  ‚û°  " + entry.getValue().getNome());
        }
        System.out.println("----------------------------------------------");
    }
